                        the continuous personal and professional development of our beneficiaries. The programme
                        includes leadership training and mentorship through internships, community involvement and
                        lifelong engagement through our volunteer network equipping young people with the leadership and
                        life skills needed to become champions in their families, communities, and schools.
                    </p>
                    <p class="border-[#25A8D6] pl-5 border-l-[14px]">
                        <span class="font-semibold">Access to Education<br></span>
