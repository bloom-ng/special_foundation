                        students in Nigeria. Through our partnership with schools, we extend our reach to disadvantaged
                        communities and lower the costs of quality education to the most vulnerable.
                    </p>
                    <p class="pb-4 border-[#25A8D6] pl-5 border-l-[14px]">
