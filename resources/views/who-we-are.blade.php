                                </p>
                                <a href="${team.link}">
                                    <img src="/images/detail_icon.svg" alt="Linkedin Icon">
                                </a>
                            </div>
                        </div>`;

                    modal.innerHTML = content;
                    modal.classList.remove('hidden');
                });
            });

